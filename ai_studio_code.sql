-- 1. TABLO TANIMLAMALARI (Garantilemek Amacıyla Kurulum)
CREATE TABLE IF NOT EXISTS firmalar (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  ad TEXT NOT NULL,
  tur TEXT NOT NULL,
  "yetkiliAd" TEXT NOT NULL,
  "yetkiliTelefon" TEXT NOT NULL,
  "alternatifTelefon" TEXT,
  whatsapp TEXT,
  eposta TEXT,
  adres TEXT NOT NULL,
  il TEXT DEFAULT 'Türkiye',
  ilce TEXT NOT NULL,
  mahalle TEXT NOT NULL,
  sokak TEXT NOT NULL,
  "kapiNo" TEXT NOT NULL,
  latitude DOUBLE PRECISION NOT NULL,
  longitude DOUBLE PRECISION NOT NULL,
  aciklama TEXT,
  durum TEXT DEFAULT 'Aktif',
  etiketler TEXT,
  "ozelAlanlar" TEXT DEFAULT '{}'
);

CREATE TABLE IF NOT EXISTS kumbaralar (
  "kumbaraNo" TEXT PRIMARY KEY,
  "firmaId" INTEGER REFERENCES firmalar(id) ON DELETE CASCADE,
  "qrKod" TEXT,
  barkod TEXT,
  "kumbaraTipi" TEXT NOT NULL,
  "yerlestirmeTarihi" BIGINT,
  "sonDegisimTarihi" BIGINT,
  "sonrakiDegisimTarihi" BIGINT,
  "periyotGun" INTEGER DEFAULT 90,
  durum TEXT DEFAULT 'Yeni',
  "toplamToplamSayisi" INTEGER DEFAULT 0,
  "toplamBagis" DOUBLE PRECISION DEFAULT 0.0,
  "sonBagisMiktari" DOUBLE PRECISION DEFAULT 0.0,
  "sonBagisTarihi" BIGINT,
  notlar TEXT
);

CREATE TABLE IF NOT EXISTS ekipler (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  isim TEXT NOT NULL,
  renk TEXT DEFAULT '#3B82F6',
  "aracBilgisi" TEXT,
  durum TEXT DEFAULT 'Aktif',
  uyeler TEXT
);

CREATE TABLE IF NOT EXISTS gorevler (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  "ekipId" INTEGER REFERENCES ekipler(id) ON DELETE CASCADE,
  tarih BIGINT,
  durum TEXT DEFAULT 'Bekliyor',
  "rotaIdleri" TEXT,
  aciklama TEXT
);

CREATE TABLE IF NOT EXISTS islem_gecmisi (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  "firmaId" INTEGER REFERENCES firmalar(id) ON DELETE CASCADE,
  "firmaAd" TEXT,
  "kumbaraNo" TEXT,
  tarih BIGINT,
  "ekipIsim" TEXT,
  "islemTuru" TEXT,
  "toplananMiktar" DOUBLE PRECISION DEFAULT 0.0,
  "eskiKumbaraNo" TEXT,
  "yeniKumbaraNo" TEXT,
  "fotoPath" TEXT,
  "imzaPath" TEXT,
  notlar TEXT
);

-- 2. ESKİ TABLOLARI SIFIRLAYIP HAZIR DEMO VERİLERİNİ EKLEME
